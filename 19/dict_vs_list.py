# Seznam studentů
students_list = ["Anna", "Petr", "Eva"]

# Slovník studentů s věkem
students_dict = {"Anna": 12, "Petr": 13, "Eva": 11}

# Přístup k datům
print(students_list[0])   # Přístup k prvnímu prvku v seznamu
print(students_dict["Anna"])  # Přístup k věku Anny ve slovníku

# Seznam je dobrý, když potřebujeme uchovávat data v pořadí.
# Slovník je lepší, když chceme rychle najít hodnoty podle klíče.
